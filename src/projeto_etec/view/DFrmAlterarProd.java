    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Bot찾o Salvar
        ProdutoController pc = new ProdutoController();
        pc.alterar(txtcod.getText(), txtnome.getText(), txtqtd.getText());
        txtcod.setText("");
        txtpes.setText("");
        txtnome.setText("");
        txtqtd.setText("");
        txtpes.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnpesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesActionPerformed
        //Bot찾o Pesquisar
        ProdutoDAO pd = new ProdutoDAO();
        ProdutoController pc=new ProdutoController();
        Produto p = new Produto();
        p.setNome_prod(txtpes.getText());
        String campo=null;
        pd.pesquisar(p);
        int cb=cbpes.getSelectedIndex();
        txtcod.setText(""+p.getId_prod());
        txtnome.setText(p.getNome_prod());
        txtqtd.setText(""+p.getQtd_estoque());
    }//GEN-LAST:event_btnpesActionPerformed

